export function বাইনারিতেরূপান্তর(পাঠ্য: string): string {
    return পাঠ্য
        .split('') // পাঠ্যকে পৃথক অক্ষরে ভাগ করে
        .map((অক্ষর) => {
            const বাইনারি = অক্ষর.charCodeAt(0).toString(2); // ASCII কোডকে বাইনারিতে রূপান্তরিত করে
            return বাইনারি.padStart(8, '0'); // নিশ্চিত করে যে প্রতিটি বাইনারি ৮ বিটের হয়
        })
        .join(' '); // সমস্ত বাইনারিকে স্পেস দিয়ে যুক্ত করে
}
