// lodash-ru.ts
import _ from 'lodash';

const lodashRu = {
    // Методы манипуляции с массивами
    добавить: (массив: any[], значение: any) => _.concat(массив, значение), // _.concat
    удалить: (массив: any[], значение: any) => _.pull(массив, значение), // _.pull
    фильтровать: (массив: any[], предикат: (значение: any) => boolean) => _.filter(массив, предикат), // _.filter
    преобразовать: (массив: any[], преобразование: (значение: any) => any) => _.map(массив, преобразование), // _.map
    уменьшить: (массив: any[], аккумулятор: (акум: any, значение: any) => any, начальное?: any) => _.reduce(массив, аккумулятор, начальное), // _.reduce
    найти: (массив: any[], предикат: (значение: any) => boolean) => _.find(массив, предикат), // _.find
    сортировать: (массив: any[], ключ: string | string[]) => _.orderBy(массив, [ключ]), // _.orderBy
    перевернуть: (массив: any[]) => _.reverse(массив), // _.reverse
    соединить: (массив: any[], разделитель: string = ',') => _.join(массив, разделитель), // _.join

    // Методы манипуляции с объектами
    получить: (объект: object, ключ: string) => _.get(объект, ключ), // _.get
    установить: (объект: object, ключ: string, значение: any) => _.set(объект, ключ, значение), // _.set
    удалитьСвойство: (объект: object, ключ: string) => _.unset(объект, ключ), // _.unset
    объединить: (...объекты: object[]) => _.merge({}, ...объекты), // _.merge
    клонировать: (объект: object) => _.cloneDeep(объект), // _.cloneDeep
    ключи: (объект: object) => _.keys(объект), // _.keys
    значения: (объект: object) => _.values(объект), // _.values
    записи: (объект: object) => _.toPairs(объект), // _.toPairs

    // Методы для строк
    заглавные: (текст: string) => _.toUpper(текст), // _.toUpper
    строчные: (текст: string) => _.toLower(текст), // _.toLower
    капитализировать: (текст: string) => _.capitalize(текст), // _.capitalize
    заменить: (текст: string, искать: string, заменять: string) => _.replace(текст, искать, заменять), // _.replace
    разделить: (текст: string, разделитель: string) => _.split(текст, разделитель), // _.split

    // Утилитарные методы
    идентичность: (значение: any) => _.identity(значение), // _.identity
    константа: (значение: any) => _.constant(значение), // _.constant
    случайное: (мин: number, макс: number) => _.random(мин, макс), // _.random
    сейчас: () => _.now(), // _.now
};

// Экспортировать модуль на русском
export default lodashRu;
