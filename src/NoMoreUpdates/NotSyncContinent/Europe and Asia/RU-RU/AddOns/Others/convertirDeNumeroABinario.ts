export function преобразоватьВБинарный(текст: string): string {
    return текст
        .split('') // Разделяет текст на отдельные символы
        .map((символ) => {
            const бинарный = символ.charCodeAt(0).toString(2); // Получает ASCII код в бинарном виде
            return бинарный.padStart(8, '0'); // Удостоверяется, что каждый бинарный код имеет 8 бит
        })
        .join(' '); // Объединяет все бинарные коды через пробел
}
