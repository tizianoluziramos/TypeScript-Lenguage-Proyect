export class স্ক্রামপরীক্ষা { 

    private কাজগুলি: string[];

    constructor(কাজগুলি: string[]) {
        this.কাজগুলি = কাজগুলি;
    }

    private স্প্রিন্টপরিকল্পনা(): void {
        console.log("\n--- স্প্রিন্ট পরিকল্পনা ---");
        console.log("সম্পন্ন করতে হবে এমন কাজগুলি:");
        this.কাজগুলি.forEach((কাজ, সূচক) => console.log(`- কাজ ${সূচক + 1}: ${কাজ}`));
    }

    private দৈনিকমিটিং(): void {
        console.log("\n--- দৈনিক মিটিং ---");
        console.log("টিমের অগ্রগতি এবং বাধাগুলি।");
    }

    private স্প্রিন্টপর্যালোচনা(): void {
        console.log("\n--- স্প্রিন্ট পর্যালোচনা ---");
        console.log("সম্পন্ন কাজগুলি এবং উন্নত পণ্য ক্লায়েন্টকে উপস্থাপন করা।");
    }

    private স্প্রিন্টরেট্রোস্পেকটিভ(): void {
        console.log("\n--- স্প্রিন্ট রেট্রোস্পেকটিভ ---");
        console.log("যা ভালো কাজ করেছে এবং কী কী উন্নতির সুযোগ ছিল তা নিয়ে আলোচনা।");
    }

    public স্প্রিন্টচালনা(): void {
        this.স্প্রিন্টপরিকল্পনা();
        this.দৈনিকমিটিং();
        this.স্প্রিন্টপর্যালোচনা();
        this.স্প্রিন্টরেট্রোস্পেকটিভ();
        console.log("\nস্প্রিন্টের সমাপ্তি।");
    }
}
