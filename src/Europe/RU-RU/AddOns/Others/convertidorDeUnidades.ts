import * as readline from 'readline';

// Функции для преобразования
export const преобразования = {
    // Длина
    метрыВСантиметры: (метры: number) => метры * 100,
    сантиметрыВМетры: (сантиметры: number) => сантиметры / 100,

    // Вес
    килограммыВФунты: (килограммы: number) => килограммы * 2.20462,
    фунтыВКилограммы: (фунты: number) => фунты / 2.20462,

    // Температура
    цельсийВФаренгейт: (цельсий: number) => (цельсий * 9/5) + 32,
    фаренгейтВЦельсий: (фаренгейт: number) => (фаренгейт - 32) * 5/9
};

// Интерфейс командной строки
export const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

export function показатьМеню(): void {
    console.log('Выберите опцию для преобразования:');
    console.log('1. Метры в Сантиметры');
    console.log('2. Сантиметры в Метры');
    console.log('3. Килограммы в Фунты');
    console.log('4. Фунты в Килограммы');
    console.log('5. Цельсий в Фаренгейт');
    console.log('6. Фаренгейт в Цельсий');
    rl.question('Выберите опцию (1-6): ', (опция) => {
        rl.question('Введите значение для преобразования: ', (значениеStr) => {
            const значение = parseFloat(значениеStr);
            выполнитьПреобразование(parseInt(опция), значение);
            rl.close();
        });
    });    
}

export function выполнитьПреобразование(опция: number, значение: number): void {
    let результат: number;

    switch (опция) {
        case 1:
            результат = преобразования.метрыВСантиметры(значение);
            console.log(`${значение} метра(ов) это ${результат} сантиметров`);
            break;
        case 2:
            результат = преобразования.сантиметрыВМетры(значение);
            console.log(`${значение} сантиметра(ов) это ${результат} метра(ов)`);
            break;
        case 3:
            результат = преобразования.килограммыВФунты(значение);
            console.log(`${значение} килограмма(ов) это ${результат} фунта(ов)`);
            break;
        case 4:
            результат = преобразования.фунтыВКилограммы(значение);
            console.log(`${значение} фунт(ов) это ${результат} килограмма(ов)`);
            break;
        case 5:
            результат = преобразования.цельсийВФаренгейт(значение);
            console.log(`${значение} градусов Цельсия это ${результат} градусов Фаренгейта`);
            break;
        case 6:
            результат = преобразования.фаренгейтВЦельсий(значение);
            console.log(`${значение} градусов Фаренгейта это ${результат} градусов Цельсия`);
            break;
        default:
            console.log('Неверный выбор');
            break;
    }
}
