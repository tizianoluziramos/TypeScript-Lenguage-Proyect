// Функция для начала доступа к камере
export async function начатьКамеруиВидео() {
    try {
        // Запрашиваем доступ к камере
        const поток = await navigator.mediaDevices.getUserMedia({ video: true });

        // Получаем первую видеопоследовательность из потока
        const видеопоследовательность = поток.getVideoTracks()[0];

        console.log('Видеопоследовательность:', видеопоследовательность);

        // Показываем информацию о видеопоследовательности
        console.log('ID видеопоследовательности:', видеопоследовательность.id);
        console.log('Метка видеопоследовательности:', видеопоследовательность.label);
        console.log('Включена ли видеопоследовательность?', видеопоследовательность.enabled);
        
        // Показываем видео в элементе <video>
        const элементВидео = document.querySelector('video');
        if (элементВидео) {
            элементВидео.srcObject = поток;
            элементВидео.play();
        }

    } catch (error) {
        console.error('Ошибка при доступе к камере:', error);
    }
}
