// os-es.ts
import {
    EOL,
    arch as 架构,
    cpus as 处理器,
    endianness as 字节顺序,
    freemem as 空闲内存,
    homedir as 用户目录,
    hostname as 主机名,
    loadavg as 平均负载,
    networkInterfaces as 网络接口,
    platform as 平台,
    release as 操作系统版本,
    tmpdir as 临时目录,
    totalmem as 总内存,
    uptime as 运行时间,
    userInfo as 用户信息,
} from 'os';

// 导出常量和函数
const 操作系统 = {
    EOL, // 结束行
    架构, // 返回处理器架构
    处理器, // 关于CPU的信息
    字节顺序, // 返回字节顺序
    空闲内存, // 返回空闲内存（字节）
    用户目录, // 返回当前用户的主目录
    主机名, // 返回主机名
    平均负载, // 返回系统的平均负载
    网络接口, // 网络接口信息
    平台, // 返回操作系统的平台
    操作系统版本, // 返回操作系统版本
    临时目录, // 返回临时目录
    总内存, // 返回总内存（字节）
    运行时间, // 返回系统运行时间
    用户信息, // 当前用户信息
};

// 导出模块
export default 操作系统;

// 辅助函数，名称翻译
export function 获取系统信息() {
    return {
        架构: 操作系统.架构(),
        主机名: 操作系统.主机名(),
        用户目录: 操作系统.用户目录(),
        空闲内存: 操作系统.空闲内存(),
        总内存: 操作系统.总内存(),
        运行时间: 操作系统.运行时间(),
        平均负载: 操作系统.平均负载(),
        网络接口: 操作系统.网络接口(),
        操作系统版本: 操作系统.操作系统版本(),
    };
}
