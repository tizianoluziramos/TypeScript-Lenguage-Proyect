import * as readline from 'readline';

// রূপান্তরের জন্য ফাংশনসমূহ
export const রূপান্তর = {
    // দৈর্ঘ্য
    মিটারথেকেসেন্টিমিটার: (মিটার: number) => মিটার * 100,
    সেন্টিমিটারথেকেমিটার: (সেন্টিমিটার: number) => সেন্টিমিটার / 100,

    // ওজন
    কিলোথেকেপাউন্ড: (কিলো: number) => কিলো * 2.20462,
    পাউন্ডথেকেকিলো: (পাউন্ড: number) => পাউন্ড / 2.20462,

    // তাপমাত্রা
    সেলসিয়াসথেকেফারেনহাইট: (সেলসিয়াস: number) => (সেলসিয়াস * 9/5) + 32,
    ফারেনহাইটথেকেসেলসিয়াস: (ফারেনহাইট: number) => (ফারেনহাইট - 32) * 5/9
};

// কমান্ড লাইন ইন্টারফেস
export const আরএল = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

export function মেনুদেখান(): void {
    console.log('একটি রূপান্তর বিকল্প নির্বাচন করুন:');
    console.log('1. মিটার থেকে সেন্টিমিটার');
    console.log('2. সেন্টিমিটার থেকে মিটার');
    console.log('3. কিলো থেকে পাউন্ড');
    console.log('4. পাউন্ড থেকে কিলো');
    console.log('5. সেলসিয়াস থেকে ফারেনহাইট');
    console.log('6. ফারেনহাইট থেকে সেলসিয়াস');
    আরএল.question('একটি বিকল্প নির্বাচন করুন (1-6): ', (বিকল্প) => {
        আরএল.question('রূপান্তর করার মান লিখুন: ', (মানস্ট্রিং) => {
            const মান = parseFloat(মানস্ট্রিং);
            রূপান্তরকরা(parseInt(বিকল্প), মান);
            আরএল.close();
        });
    });    
}

export function রূপান্তরকরা(বিকল্প: number, মান: number): void {
    let ফলাফল: number;

    switch (বিকল্প) {
        case 1:
            ফলাফল = রূপান্তর.মিটারথেকেসেন্টিমিটার(মান);
            console.log(`${মান} মিটার ${ফলাফল} সেন্টিমিটার সমান`);
            break;
        case 2:
            ফলাফল = রূপান্তর.সেন্টিমিটারথেকেমিটার(মান);
            console.log(`${মান} সেন্টিমিটার ${ফলাফল} মিটার সমান`);
            break;
        case 3:
            ফলাফল = রূপান্তর.কিলোথেকেপাউন্ড(মান);
            console.log(`${মান} কিলো ${ফলাফল} পাউন্ড সমান`);
            break;
        case 4:
            ফলাফল = রূপান্তর.পাউন্ডথেকেকিলো(মান);
            console.log(`${মান} পাউন্ড ${ফলাফল} কিলো সমান`);
            break;
        case 5:
            ফলাফল = রূপান্তর.সেলসিয়াসথেকেফারেনহাইট(মান);
            console.log(`${মান} ডিগ্রি সেলসিয়াস ${ফলাফল} ডিগ্রি ফারেনহাইট সমান`);
            break;
        case 6:
            ফলাফল = রূপান্তর.ফারেনহাইটথেকেসেলসিয়াস(মান);
            console.log(`${মান} ডিগ্রি ফারেনহাইট ${ফলাফল} ডিগ্রি সেলসিয়াস সমান`);
            break;
        default:
            console.log('অবৈধ বিকল্প');
            break;
    }
}
