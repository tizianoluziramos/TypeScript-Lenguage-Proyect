// ভেরিয়েবল সংরক্ষণ করার জন্য অবজেক্ট
const ভেরিয়েবল: Record<string, any> = {};

// একটি ভেরিয়েবল সংজ্ঞায়িত করার ফাংশন "নাম: প্রকার = মান" ফরম্যাটে
function ভেরিয়েবলসংজ্ঞায়িত(অ্যাসাইনমেন্ট: string) {
  // নাম, প্রকার এবং মান বের করতে একটি নিয়মিত এক্সপ্রেশন ব্যবহার করা
  const regex = /^(?<নাম>\w+):\s*(?<প্রকার>\w+)\s*=\s*(.+)$/;
  const match = অ্যাসাইনমেন্ট.match(regex);

  if (match) {
    const নাম = match[1]; // প্রথম ক্যাপচার গ্রুপ
    const প্রকার = match[2]; // দ্বিতীয় ক্যাপচার গ্রুপ
    const মান = match[3]; // তৃতীয় ক্যাপচার গ্রুপ

    // মানকে উপযুক্ত প্রকারে রূপান্তর করা
    let মানরূপান্তরিত: any;
    switch (প্রকার.toLowerCase()) {
      case 'number':
        মানরূপান্তরিত = Number(মান);
        break;
      case 'string':
        মানরূপান্তরিত = String(মান);
        break;
      case 'boolean':
        মানরূপান্তরিত = মান.toLowerCase() === 'true';
        break;
      default:
        console.log(`ত্রুটি: '${প্রকার}' প্রকার চেনা যায়নি.`);
        return;
    }

    // ভেরিয়েবলটি অবজেক্টে সংরক্ষণ করা
    ভেরিয়েবল[নাম] = মানরূপান্তরিত;
  } else {
    console.log('ত্রুটি: অসাংগত অ্যাসাইনমেন্ট ফরম্যাট। "নাম: প্রকার = মান" ব্যবহার করুন।');
  }
}

